#include "libft.h"

t_list	*ft_listlast(t_list *lst)
{
	t_list	*p;
	int		i;

	p = lst;
	i = 0;
	if (p)
	{
		while (p->next)
		{
			p = p->next;
			i++;
		}
	}
	return (p);
}
